// Map current full language string (from AppContext) to translation keys
export const getLangCode = (language) => {
  if (!language) return "ko";
  const normalized = language.toLowerCase();
  if (normalized.includes("eng") || normalized.includes("영어")) return "en";
  if (normalized.includes("esp") || normalized.includes("스페인") || normalized.includes("spa")) return "es";
  if (normalized.includes("ara") || normalized.includes("아랍") || normalized.includes("ar")) return "ar";
  if (normalized.includes("chi") || normalized.includes("중국") || normalized.includes("zh")) return "zh";
  if (normalized.includes("vie") || normalized.includes("베트남") || normalized.includes("vi")) return "vi";
  if (normalized.includes("ind") || normalized.includes("인도") || normalized.includes("id")) return "id";
  return "ko"; // default
};

export const getTranslation = (key, language = "한국어") => {
  const code = getLangCode(language);
  const dict = translations[code] || translations["ko"];
  return dict[key] || translations["ko"][key] || key;
};

export default getTranslation;
